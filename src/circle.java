public class circle {

        double radius = 10;
        double diameter = 20;
        double pi = 3.14;


        public void lenght_circle () {
        double lenght = pi * diameter;
        System.out.println(lenght);
    }
        public void area_circle () {
        double area = Math.sqrt(radius);
        System.out.println(area);
    }


}
